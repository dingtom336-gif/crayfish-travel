// TestHealthEndpoint verifies the /health liveness probe returns 200.
func TestHealthEndpoint(t *testing.T) {
	h := &Handlers{}
	r := Setup("test", h, nil, "")

	w := httptest.NewRecorder()
	req, _ := http.NewRequest("GET", "/health", nil)
	r.ServeHTTP(w, req)

	if w.Code != http.StatusOK {
		t.Errorf("expected status 200, got %d", w.Code)
	}

	var body map[string]interface{}
	if err := json.Unmarshal(w.Body.Bytes(), &body); err != nil {
		t.Fatalf("failed to parse response body: %v", err)
	}

	if body["status"] != "ok" {
		t.Errorf("expected status 'ok', got %v", body["status"])
	}

	// Trace ID should be present (auto-generated by middleware)
	if _, exists := body["trace_id"]; !exists {
		t.Error("expected trace_id in response")
	}
}

// TestHealthEndpointWithTraceID verifies that a client-provided X-Trace-ID is echoed back.
func TestHealthEndpointWithTraceID(t *testing.T) {
	h := &Handlers{}
	r := Setup("test", h, nil, "")

	w := httptest.NewRecorder()
	req, _ := http.NewRequest("GET", "/health", nil)
	req.Header.Set("X-Trace-ID", "test-trace-123")
	r.ServeHTTP(w, req)

	if w.Code != http.StatusOK {
		t.Errorf("expected status 200, got %d", w.Code)
	}

	// Response header should echo the trace ID
	if got := w.Header().Get("X-Trace-ID"); got != "test-trace-123" {
		t.Errorf("expected X-Trace-ID 'test-trace-123', got %q", got)
	}
}


// TestAdminEndpointRequiresToken verifies that admin routes need authentication.
func TestAdminEndpointRequiresToken(t *testing.T) {
	h := &Handlers{}
	r := Setup("test", h, nil, "secret-admin-token")

	// Without token
	w := httptest.NewRecorder()
	req, _ := http.NewRequest("POST", "/api/v1/admin/fund-pool/seed", nil)
	r.ServeHTTP(w, req)

	if w.Code != http.StatusUnauthorized {
		t.Errorf("expected 401 without token, got %d", w.Code)
	}

	// With wrong token
	w = httptest.NewRecorder()
	req, _ = http.NewRequest("POST", "/api/v1/admin/fund-pool/seed", nil)
	req.Header.Set("X-Admin-Token", "wrong-token")
	r.ServeHTTP(w, req)

	if w.Code != http.StatusUnauthorized {
		t.Errorf("expected 401 with wrong token, got %d", w.Code)
	}
}

